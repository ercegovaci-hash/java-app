    private void jButtonOtkaziPrijavljivanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOtkaziPrijavljivanjeActionPerformed
       
        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da otkazete prijavljivanje?", "Otkazivanje", JOptionPane.YES_NO_OPTION);
        
        if(result==JOptionPane.NO_OPTION){
            return;
        }
        
        if(result==JOptionPane.YES_OPTION){
        
            try {
                Kontrolor.getInstance().deletePrijavljivanje(p);
                PretragaPrijavljivanjeForma forma= (PretragaPrijavljivanjeForma) getParent();
                forma.refreshTable();
                JOptionPane.showMessageDialog(this, "Sistem je obrisao prijavljivanje.");
                this.dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne mo≈æe da obri≈°e prijavljivanje.");

                Logger.getLogger(DetaljiPrijavljivanjaForma.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
        
        
        
    }//GEN-LAST:event_jButtonOtkaziPrijavljivanjeActionPerformed

    private void jButtonDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajActionPerformed
        
       NastavniProgram izabraniProgram=new NastavniProgram();
        
       String nazivPrograma= (String) jComboBoxProgrami.getSelectedItem();
       for(int i=0;i<programi.size();i++){
            if(programi.get(i).getNaziv().equals(nazivPrograma)){
                
                izabraniProgram=programi.get(i);
            }
        }
       
        int brojESPB= Integer.parseInt(jTextFieldBrojESPB.getText());
        double cenaESPB= Double.parseDouble(jTextFieldCenaESPB.getText());
        
        Kurs k= new Kurs(-1, brojESPB, cenaESPB, izabraniProgram, p);
        TableModelKursevi tm = (TableModelKursevi) jTableKursevi.getModel();
        tm.dodajKurs(k);
        
        ukupnaCena= tm.vratiUkupnuCenu();
        jTextFieldUkupnaCena.setText(String.valueOf(ukupnaCena));
        
    }//GEN-LAST:event_jButtonDodajActionPerformed

