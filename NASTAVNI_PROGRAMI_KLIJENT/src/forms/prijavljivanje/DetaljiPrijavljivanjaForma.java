    private void jButtonIzmeniPrijavljivanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIzmeniPrijavljivanjeActionPerformed
              Student izabraniStudent=new Student();  
        try {
           
            SimpleDateFormat sdf= new SimpleDateFormat("dd.MM.yyyy");
            Date datum =sdf.parse(jTextFieldDatum.getText());
            TableModelKursevi tm= (TableModelKursevi) jTableKursevi.getModel();
            p.setStudent(izabraniStudent);
            p.setDatumPrijavljivanja(datum); 
            p.setKurs(tm.getLista());
            p.setUkupnaCena(ukupnaCena);
            
            Kontrolor.getInstance().updatePrijavljivanje(p);
            PretragaPrijavljivanjeForma forma= (PretragaPrijavljivanjeForma) getParent();
            forma.refreshTable();
            
            JOptionPane.showMessageDialog(this, "Sistem je izmenio prijavljivanje.");
            this.dispose();
            
        } catch (SocketException ex) {
        JOptionPane.showMessageDialog(this, "Veza sa serverom je prekinuta. Poku≈°ajte ponovo kasnije.", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
    } catch (Exception ex) {
        Logger.getLogger(DetaljiPrijavljivanjaForma.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Sistem ne mo≈æe da izmeni prijavljivanje.");
    }
        
        
    }//GEN-LAST:event_jButtonIzmeniPrijavljivanjeActionPerformed

