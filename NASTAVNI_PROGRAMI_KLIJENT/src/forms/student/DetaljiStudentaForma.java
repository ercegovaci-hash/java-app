    private void jButtonIZMENIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIZMENIActionPerformed

        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da izmeni studenta.");
            return;
        }

        String ime = jTextField1.getText();
        String prezime = jTextField2.getText();
        String email = jTextField3.getText();

        
        s.setIme(ime);
        s.setPrezime(prezime);
        s.setEmail(email);

        try {
            Kontrolor.getInstance().updateStudent(s);
            PretragaStudentaForma form = (PretragaStudentaForma) getParent();
            form.osveziTabelu();
            form.popuniStudente();
            JOptionPane.showMessageDialog(this, "Sistem je izmenio podatke studenta.");
            this.dispose();

        } catch (Exception ex) {
            Logger.getLogger(DetaljiStudentaForma.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButtonIZMENIActionPerformed

