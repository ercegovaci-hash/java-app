    private void jButtonOBRISIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOBRISIActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da obrisete korisnika?", "Brisanje korisnika", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }
        if (result == JOptionPane.YES_OPTION) {

            try {
                Kontrolor.getInstance().deleteStudent(s);
                PretragaStudentaForma formar = (PretragaStudentaForma) getParent();
                formar.osveziTabelu();
                formar.popuniStudente();
                JOptionPane.showMessageDialog(this, "Sistem je obrisao studenta!");
                this.dispose();

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da obriše studenta.");
                Logger.getLogger(DetaljiStudentaForma.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jButtonOBRISIActionPerformed

