    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed

        int result= JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da se odjavite?", "Odjavi se",JOptionPane.YES_NO_OPTION);

        if(result==JOptionPane.NO_OPTION){
            return;
        }

        if(result==JOptionPane.YES_OPTION){

            new LOGIN_form().setVisible(true);

            try {

                Kontrolor.getInstance().logout(ulogovani);
                this.dispose();

            } catch (Exception ex) {
                Logger.getLogger(Glavna_formaa.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jMenuItem4ActionPerformed

    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
        Student izabraniStudent=new Student();
        try {

            if(jTextFieldDatum.getText().isEmpty()){

                JOptionPane.showMessageDialog(this, "Morate uneti datum");
                return;
            }

            String student = (String) jComboBoxStudenti.getSelectedItem();
            for(int i=0;i<studenti.size();i++){
                if(studenti.get(i).getIme().concat(" "+studenti.get(i).getPrezime()).equals(student.toString())){
                    izabraniStudent=studenti.get(i);
             }
            }
            SimpleDateFormat sf= new SimpleDateFormat("dd.MM.yyyy");

            Date date = sf.parse(jTextFieldDatum.getText());

            TableModelKursevi tm = (TableModelKursevi) jTable1.getModel();

            Prijavljivanje prijavljivanje = new Prijavljivanje(-1, date, ukupnaCena, ulogovani, izabraniStudent, tm.getLista());

            Kontrolor.getInstance().addNewPrijavljivanje(prijavljivanje);
            resetujFormu();
            JOptionPane.showMessageDialog(this, "Sistem је kreirao prijavljivanje");

        } catch (ParseException ex) {
            Logger.getLogger(Glavna_formaa.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(Glavna_formaa.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Sistem ne može da kreira prijavljivanje.");
        }
    }//GEN-LAST:event_jButtonSacuvajActionPerformed

