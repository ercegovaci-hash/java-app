    public void popuniStudente() {
        
        try {
            jComboBoxStudenti.removeAllItems();
            studenti = Kontrolor.getInstance().getAllStudents();
            for (Student s : studenti) {
                jComboBoxStudenti.addItem(s.toString());
            }
        } catch (Exception ex) {
            Logger.getLogger(Glavna_formaa.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

    public void popuniPrograme() {
        try {
            jComboBoxProgrami.removeAllItems();
            
            programi=Kontrolor.getInstance().getAllNastavniPrograms();
            
            for(NastavniProgram np:programi){
                jComboBoxProgrami.addItem(np.toString());
            }
        } catch (Exception ex) {
            Logger.getLogger(Glavna_formaa.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
  
    
     

